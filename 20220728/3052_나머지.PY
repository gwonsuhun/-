# 두 자연수 A와 B가 있을 때, A%B는 A를 B로 나눈 나머지 이다. 예를 들어, 7, 14, 27, 38을 3으로 나눈 나머지는 1, 2, 0, 2이다. 
                         




num = 42 #나눌수
 #나눈 결과값
surplus = []
for i in range(1,11): #나눌횟수
    number = int(input()) # 42로 나눌 숫자
    result = number % num # 입력받은값을 나눠주고 리스트에 넣는다
    surplus.append(result)
print(len(set(surplus))) # set으로 리스트의값의 중복을제거하면 서로다른 나머지를구할수있다 그러고 다른숫자만  남으니까 
                         #리스트안의 길이가 서로다른나머지의숫자의 갯수다





